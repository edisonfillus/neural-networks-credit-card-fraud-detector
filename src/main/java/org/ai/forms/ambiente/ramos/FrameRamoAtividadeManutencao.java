    public void updateTableEstabelecimentosRamo() {
        updateListEstabelecimentosRamo();
        tableEstabelecimentos.setModel(modelEstabelecimentosRamo);
        tableEstabelecimentos.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        tableEstabelecimentos.setRowSelectionAllowed(true);
        tableEstabelecimentos.setColumnSelectionAllowed(false);
        ColumnResizer.adjustColumnPreferredWidths(tableEstabelecimentos);
        tableEstabelecimentos.revalidate();
        lblTotalEstabelecimentos.setText(Integer.toString(listEstabelecimentosRamo.size()));
    }

    private void updateListEstabelecimentosRamo() {
        listEstabelecimentosRamo = ramo.getEstabelecimentos();
        String[] collumnNames = new String[]{"Nome", "Estado","Cidade"};
        String[][] data = new String[listEstabelecimentosRamo.size()][];
        for (int i = 0; i < listEstabelecimentosRamo.size(); i++) {
            data[i] = new String[]{
                        listEstabelecimentosRamo.get(i).getNome(),
                        listEstabelecimentosRamo.get(i).getCidade().getEstado().getNome(),
                        listEstabelecimentosRamo.get(i).getCidade().getNome()
                    };
        }
        modelEstabelecimentosRamo = new DefaultTableModel(data, collumnNames) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
    }

