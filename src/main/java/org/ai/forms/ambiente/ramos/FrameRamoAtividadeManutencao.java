private void btnInserirEstabelecimentoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInserirEstabelecimentoActionPerformed
    FrameEstabelecimentoCadastro frame = new FrameEstabelecimentoCadastro(ramo);
    formPrincipal.getDesktopPane().add(frame);
    frame.setLocation((int) this.getBounds().getX(), (int) this.getBounds().getY() + this.getHeight());
    frame.setVisible(true);
    frame.addInternalFrameListener(new InternalFrameListener() {
        public void internalFrameOpened(InternalFrameEvent e) {}
        public void internalFrameClosing(InternalFrameEvent e) {}
        public void internalFrameClosed(InternalFrameEvent e) {
            ramo.setEstabelecimentos(RamoAtividade.find(ramo.getId()).getEstabelecimentos());
            updateTableEstabelecimentosRamo();
        }
        public void internalFrameIconified(InternalFrameEvent e) {}
        public void internalFrameDeiconified(InternalFrameEvent e) {}
        public void internalFrameActivated(InternalFrameEvent e) {}
        public void internalFrameDeactivated(InternalFrameEvent e) {}
    });
}//GEN-LAST:event_btnInserirEstabelecimentoActionPerformed

private void tableEstabelecimentosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableEstabelecimentosMouseClicked
    if (tableEstabelecimentos.getSelectedRow() >= 0) {
        Estabelecimento estabelecimento = listEstabelecimentosRamo.get(tableEstabelecimentos.getSelectedRow());
        FrameEstabelecimentoManutencao frame = new FrameEstabelecimentoManutencao(formPrincipal,estabelecimento.getId());
        formPrincipal.getDesktopPane().add(frame);
        frame.setLocation((int) this.getBounds().getX(), (int) this.getBounds().getY() + this.getHeight());
        frame.setVisible(true);
        frame.addInternalFrameListener(new InternalFrameListener() {
                public void internalFrameOpened(InternalFrameEvent e) {}
                public void internalFrameClosing(InternalFrameEvent e) {}
                public void internalFrameClosed(InternalFrameEvent e) {
                    ramo.setEstabelecimentos(RamoAtividade.find(ramo.getId()).getEstabelecimentos());
                    updateTableEstabelecimentosRamo();
                }
                public void internalFrameIconified(InternalFrameEvent e) {}
                public void internalFrameDeiconified(InternalFrameEvent e) {}
                public void internalFrameActivated(InternalFrameEvent e) {}
                public void internalFrameDeactivated(InternalFrameEvent e) {}
            });
    }
}//GEN-LAST:event_tableEstabelecimentosMouseClicked

private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
    int resposta = JOptionPane.showConfirmDialog(this, "Salvar alterações do Ramo de Atividade " + ramo.getNome() + "?", "Atenção", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
    if (resposta == JOptionPane.YES_OPTION) {
        ramo.setNome(txtNome.getText());
        boolean updated = ramo.update();
        if (updated) {
            JOptionPane.showMessageDialog(this, "Ramo " + ramo.getNome() + " salvo com sucesso");
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao salvar Ramo " + ramo.getNome());
        }
        dispose();
    }
}//GEN-LAST:event_btnSalvarActionPerformed

private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
    int resposta = JOptionPane.showConfirmDialog(this, "Confirma exclusão do Ramo de Atividade " + ramo.getNome() + "?", "Atenção", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
    if (resposta == JOptionPane.YES_OPTION) {
        boolean deleted = ramo.delete();
        if (deleted) {
            JOptionPane.showMessageDialog(this, "Ramo de Atividade " + ramo.getNome() + " excluido com sucesso");
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao excluir ramo " + ramo.getNome());
        }
        dispose();
    }
}//GEN-LAST:event_btnExcluirActionPerformed

