     public void updateTableClientes() {
        updateListClientes();
        tableClientes.setModel(modelClientes);
        tableClientes.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        tableClientes.setRowSelectionAllowed(true);
        tableClientes.setColumnSelectionAllowed(false);
        ColumnResizer.adjustColumnPreferredWidths(tableClientes);
        tableClientes.revalidate();
        lblTotalClientes.setText(Integer.toString(listClientes.size()));
    }

    private void updateListClientes() {
        listClientes = Cliente.findAll();
        String[] collumnNames = new String[]{"Nome","Cidade","Renda", "Idade", "Cart√µes"};
        String[][] data = new String[listClientes.size()][5];
        for (int i = 0; i < listClientes.size(); i++) {
            data[i] = new String[]{
                        listClientes.get(i).getNome(),
                        listClientes.get(i).getCidade().getNome(),
                        new DecimalFormat("R$ #,###,###.00").format(listClientes.get(i).getRenda()),
                        Integer.toString(listClientes.get(i).getIdade()),
                        Integer.toString(listClientes.get(i).getCartoes().size())
                    };
        }
        modelClientes = new DefaultTableModel(data, collumnNames) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };

    }

