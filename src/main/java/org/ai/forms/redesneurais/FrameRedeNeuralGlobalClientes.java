private void btnAnalisarClientesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAnalisarClientesActionPerformed
    final FrameRedeNeuralGlobalClientes frame = this;
    final AnalisadorPerfilCliente analisador = new AnalisadorPerfilCliente(Cliente.findAll());

    analisador.addAnaliseListener(new AnaliseListener() {

        public void analiseIniciada() {
            prgAnalisePerfilCliente.setValue(0);
            btnAnalisarClientes.setEnabled(false);
        }

        public void progressoAtualizado() {
            prgAnalisePerfilCliente.setValue(((analisador.getTotalClientesAnalisados()) / analisador.getTotalClientes()) * 100);
            lblTotalClientesAnalisados.setText(Integer.toString(analisador.getTotalClientesAnalisados()));
        }

        public void analiseFinalizada() {
            prgAnalisePerfilCliente.setValue(100);
            btnAnalisarClientes.setEnabled(true);


            SwingUtilities.invokeLater(new Runnable() {

                public void run() {
                    JOptionPane.showMessageDialog(frame, "Analise Finalizada");
                }
            });
        }
    });

    Thread threadAnalisador = new Thread(analisador);
    threadAnalisador.setName("Analisador");
    threadAnalisador.setPriority(Thread.MIN_PRIORITY);
    threadAnalisador.start();
}//GEN-LAST:event_btnAnalisarClientesActionPerformed

