private void btnCarregarCasosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCarregarCasosActionPerformed
    List<Caso> casosTreinamento = new ArrayList<Caso>();
    List<Caso> casosTeste = new ArrayList<Caso>();
    for (Cliente cliente : clientes) {
        casosTreinamento.addAll(cliente.getAnalisePerfil().recuperarCasosTreinamento(cliente, TipoCaso.TREINAMENTO));
        casosTeste.addAll(cliente.getAnalisePerfil().recuperarCasosTreinamento(cliente, TipoCaso.TESTE));
    }
    redeGlobalClientes.getDadosTreinamento().setLabels(CasoCliente.getLabels());
    redeGlobalClientes.getDadosTreinamento().setListCasosTreinamento(casosTreinamento);
    redeGlobalClientes.getDadosTreinamento().setListCasosTeste(casosTeste);
    redeGlobalClientes.update();
    JOptionPane.showMessageDialog(this, "Casos carregados com sucesso");
}//GEN-LAST:event_btnCarregarCasosActionPerformed

