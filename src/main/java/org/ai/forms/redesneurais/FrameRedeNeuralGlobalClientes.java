    private void updateLabelsCasos(){
        transacoes = Transacao.findAll();
        int totalTreinamento = 0;
        int totalTeste = 0;
        for (Transacao transacao : transacoes) {
            if(transacao.isFlagTeste()){
                totalTeste++;
            }
            if(transacao.isFlagTreinamento()){
                totalTreinamento++;
            }
        }
        lblTotalCasosTreinamento.setText(Integer.toString(totalTreinamento));
        lblTotalCasosTeste.setText(Integer.toString(totalTeste));
    }

    private void updateLabelsRedeNeural() {
        lblStatusRedeNeural.setText(redeGlobalClientes.getStatus().getNome());
        if (redeGlobalClientes.getStatus() == StatusRedeNeural.TREINADA) {
            lblDhUltimoTreinamentoRedeNeural.setText(new SimpleDateFormat("dd/MM/yyyy HH:mm:ss SSS").format(redeGlobalClientes.getDadosTreinamento().getDhUltimoTreinamento().getTime()));
        } else {
            lblDhUltimoTreinamentoRedeNeural.setText("Não possui treinamento");
        }
    }

